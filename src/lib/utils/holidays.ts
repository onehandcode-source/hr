/** 빠른 Set 조회용 */
export const HOLIDAY_SET = new Set<string>(Object.keys(HOLIDAY_MAP));

export function isHoliday(dateStr: string): boolean {
	return HOLIDAY_SET.has(dateStr);
}

/**
 * 두 날짜 사이(포함)의 공휴일 목록을 반환합니다 (주말 제외, 순수 평일 공휴일만).
 */
export function getHolidaysInRange(
	start: Date,
	end: Date,
): Array<{ date: string; name: string }> {
	const result: Array<{ date: string; name: string }> = [];
	const endTs = end.getTime();
	const cur = new Date(start);
	cur.setHours(0, 0, 0, 0);

	while (cur.getTime() <= endTs) {
		const dateStr = cur.toISOString().slice(0, 10);
		const dow = cur.getDay();
		// 주말이 아닌 공휴일만 (주말 공휴일은 어차피 연차에서 제외됨)
		if (dow !== 0 && dow !== 6 && HOLIDAY_MAP[dateStr]) {
			result.push({ date: dateStr, name: HOLIDAY_MAP[dateStr] });
		}
		cur.setDate(cur.getDate() + 1);
	}
	return result;
}
